            <p>
                The Audio Visualizer utilizes the FFT Algorithm to convert audio into frequency-domain information, which is then used to create different graphics to visualize the audio being played.
            
                <br><br>
            
                Using information from the FFT, I created 3D objects which generate, move, and change colors according to the audio's frequencies. Additionally, I created a fluctuating polar graph where each point is mapped to a frequency value. Finally, at the bottom of the screen is a 3D Terrain made using Perlin Noise that's responsive to the audio. A demo can be viewed <a href="https://youtu.be/7KIApzMEXdQ">here <sup><i class='fas fa-external-link-alt arrow'></i></sup></a>.
